public class CalcBasicFunctions
{
    public double addTwoNumbers (double numOne, double numTwo)
    {
       double sum;

       sum = numOne + numTwo;

       return sum;
    }

    public double subTwoNumbers (double numOne, double numTwo)
    {
        double difference;

        difference = numOne - numTwo;

        return difference;
    }

    public double multTwoNumebrs (double numOne, double numTwo)
    {
        double product;

        product = numOne * numTwo;

        return product;
    }

    public double divideTwoNumbers (double numOne, double numTwo)
    {
        double quotient;

        quotient = numOne/numTwo;

        return quotient;
    }


}
